package com.example.tugas3.models;

public class WayangJawa {
    public String wayangJawa;
    public int gambar;

    public WayangJawa(String wayangJawa, int gambar) {
        this.wayangJawa = wayangJawa;
        this.gambar = gambar;
    }

    public String getWayangJawa() {
        return wayangJawa;
    }

    public void setWayangJawa(String wayangJawa) {
        this.wayangJawa = wayangJawa;
    }

    public int getGambar() {
        return gambar;
    }

    public void setGambar(int gambar) {
        this.gambar = gambar;
    }
}
